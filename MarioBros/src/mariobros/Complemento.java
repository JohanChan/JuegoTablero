package mariobros;

public class Complemento {
    String nombre="";
    String imagen="";
    int vida=0;
    int posX=0;
    int posY=0;
    public Complemento(String nombre,String imagen, int vida, int posX,int posY){
        this.imagen=imagen;
        this.nombre=nombre;
        this.posX=posX;
        this.posY=posY;
        this.vida=vida;
    }
}
